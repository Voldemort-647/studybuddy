function createLesson(standard, subject, chapter, theme, examples) {
  const lines = [
    `${chapter} is an important ${subject} chapter for Class ${standard}.`,
    `This chapter mainly teaches ${theme}.`,
    `A strong understanding of ${chapter} helps students build confidence in later topics.`,
    `The first step is to identify the main idea behind the chapter in simple language.`,
    `Students should connect each new rule to a familiar example before memorising anything.`,
    `In classroom learning, teachers usually begin by defining the concept clearly.`,
    `After that, they show how the concept changes when values, situations, or conditions change.`,
    `One useful way to learn ${chapter} is to write the key idea in your own words.`,
    `Another useful method is to solve one easy example before attempting mixed questions.`,
    `This chapter often appears in exams because it checks both understanding and careful steps.`,
    `Common mistakes happen when students rush or skip the basic rule of the chapter.`,
    `To avoid that, read the question slowly and decide which concept from ${chapter} is being tested.`,
    `A practical example of ${chapter} can be seen in ${examples[0]}.`,
    `You can also connect it to ${examples[1]} to remember the idea more naturally.`,
    `When revision time is short, review the most important rule and one solved example from ${examples[2]}.`,
    `If students practise regularly, ${chapter} becomes easier, faster, and much more predictable in tests.`,
  ];

  return lines.join("\n");
}

function createNotes(chapter, theme) {
  return [
    `${chapter} focuses on ${theme}.`,
    `Start every answer by identifying the correct concept or rule.`,
    `Use one clear example to check whether your understanding is correct.`,
    `Watch for common step errors, especially when solving under exam pressure.`,
    `Revise this chapter through short daily practice instead of one long session.`,
  ];
}

function createQuiz(chapter, subject, theme, examples) {
  return [
    {
      question: `What is the main focus of the chapter ${chapter}?`,
      options: [
        theme,
        "Planning a picnic",
        "Cooking a recipe",
        "Learning a foreign language",
      ],
      answer: theme,
    },
    {
      question: `Which subject does ${chapter} belong to in this prototype?`,
      options: [subject, "History", "Geography", "Computer Science"],
      answer: subject,
    },
    {
      question: `Which study habit best helps with ${chapter}?`,
      options: [
        "Practising the key rule with examples",
        "Skipping solved examples",
        "Memorising random words only",
        "Ignoring mistakes completely",
      ],
      answer: "Practising the key rule with examples",
    },
    {
      question: `Which everyday context is used in the lesson for ${chapter}?`,
      options: [examples[0], "Deep sea diving", "Rocket repair", "Forest camping"],
      answer: examples[0],
    },
    {
      question: `What is the safest way to answer a question from ${chapter}?`,
      options: [
        "Choose the concept first and solve step by step",
        "Write the final answer without checking",
        "Guess quickly and move on",
        "Avoid using the chapter rule",
      ],
      answer: "Choose the concept first and solve step by step",
    },
  ];
}

function buildStandardData(subjects) {
  return Object.fromEntries(
    Object.entries(subjects).map(([subject, chapters]) => [
      subject,
      Object.fromEntries(
        chapters.map(({ chapter, theme, examples }) => [
          chapter,
          {
            lesson: createLesson(
              Object.keys(chapterBlueprints).find(
                (standard) => chapterBlueprints[standard][subject] === chapters
              ),
              subject,
              chapter,
              theme,
              examples
            ),
            notes: createNotes(chapter, theme),
            quiz: createQuiz(chapter, subject, theme, examples),
          },
        ])
      ),
    ])
  );
}

